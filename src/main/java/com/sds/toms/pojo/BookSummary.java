package com.sds.toms.pojo;

public class BookSummary {

	private Integer approvalbook;
	private Integer declinebook;
	private Integer draftbook;
	private Integer totalbook;
	
	public Integer getApprovalbook() {
		return approvalbook;
	}
	public void setApprovalbook(Integer approvalbook) {
		this.approvalbook = approvalbook;
	}
	public Integer getDeclinebook() {
		return declinebook;
	}
	public void setDeclinebook(Integer declinebook) {
		this.declinebook = declinebook;
	}
	public Integer getDraftbook() {
		return draftbook;
	}
	public void setDraftbook(Integer draftbook) {
		this.draftbook = draftbook;
	}
	public Integer getTotalbook() {
		return totalbook;
	}
	public void setTotalbook(Integer totalbook) {
		this.totalbook = totalbook;
	}
	
	
}
