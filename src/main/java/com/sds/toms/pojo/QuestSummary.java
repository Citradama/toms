package com.sds.toms.pojo;

public class QuestSummary {

	private Integer approvalquest;
	private Integer declinequest;
	private Integer draftquest;
	private Integer totalquest;
	
	public Integer getApprovalquest() {
		return approvalquest;
	}
	public void setApprovalquest(Integer approvalquest) {
		this.approvalquest = approvalquest;
	}
	public Integer getDeclinequest() {
		return declinequest;
	}
	public void setDeclinequest(Integer declinequest) {
		this.declinequest = declinequest;
	}
	public Integer getDraftquest() {
		return draftquest;
	}
	public void setDraftquest(Integer draftquest) {
		this.draftquest = draftquest;
	}
	public Integer getTotalquest() {
		return totalquest;
	}
	public void setTotalquest(Integer totalquest) {
		this.totalquest = totalquest;
	}
	
	
}
